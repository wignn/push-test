package Tugas7;

public class LoopWhileHuruf {
 public static void main(String[] args) {
        System.out.println("Huruf Z - A menggunakan while:");
        char c = 'Z';
        while (c >= 'A') {
            System.out.print(c + " ");
            c--;
        }
        System.out.println(); 
    }
}
